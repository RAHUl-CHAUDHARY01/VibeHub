const generateAccessAndRefereshTokens = async (userId) => {
    try {
        const user = await User.findById(userId)
        const accessToken = user.generateAccessToken()
        const refreshToken = user.generateRefreshToken()

        user.refreshToken = refreshToken
        await user.save({ validateBeforeSave: false })          
        user.refreshToken = refreshToken
        return { accessToken, refreshToken }


    } catch (error) {
        throw new ApiError(500, "Something went wrong while generating referesh and access token")
    }
}


const registerUser = asyncHandler(async (req, res) => {

    const { username, email, password, fullName } = req.body
    // console.log(req.body)
    if (fullName === "") {
        throw new ApiError(400, "fullName is required")
    }
    if (email === "") {
        throw new ApiError(400, "email is required")
    }
    if (password === "") {
        throw new ApiError(400, "password is required")
    }
    if (username === "") {
        throw new ApiError(400, "username is required")
    }

    const existedUser = await User.findOne({                     //it will find the first similar entry if found by it
        $or: [{ email }, { username }]
    })

    if (existedUser) {
        throw new ApiError(409, "User with same user name or email exists")
    }

    const avtarLocalPath = req.files?.avtar[0].path;
  

    let coverImageLocalPath;

    if (req.files && Array.isArray(req.files.coverImage) && req.files.coverImage.length > 0) {
        coverImageLocalPath = req.files.coverImage[0].path
    }

    if (!avtarLocalPath) {
        throw new ApiError(400, "Avtar is required")
    }

    const avtar = await uploadOnCloudinary(avtarLocalPath)
    const coverImage = await uploadOnCloudinary(coverImageLocalPath)

    if (!avtar) {
        throw new ApiError(400, "Avtar is required")
    }
    console.log(username);  
    const user = await User.create({
        fullName,
        avtar: avtar.url,
        coverImage: coverImage?.url || "",
        email,
        password,
        username: username.toLowerCase()
    })

    const createdUser = await User.findById(user._id).select("-password -refreshTokens")
    if (!createdUser) {
        throw new ApiError(500, "something went wrong while registering the user")
    }

    return res.status(201).json(
        new ApiResponse(200, createdUser, "User registered Successfully!!!")
    )
})



const loginUser = asyncHandler(async (req, res) => {


    const {email,username,password} = req.body;
    console.log(req.body);


    if (!username && !email) {
        throw new ApiError(400, "username or email is required")
    }

  

    const user = await User.findOne({
        $or: [{ username }, { email }]
    })



    if (!user) {
        throw new ApiError(404, "User does not exist")
    }

    const isPasswordValid = await user.isPasswordCorrect(password)


    if (!isPasswordValid) {
        throw new ApiError(401, "Invalid user credentials")
    }

    const { accessToken, refreshToken } = await generateAccessAndRefereshTokens(user._id)

    const loggedInUser = await User.findById(user._id).select("-password -refreshToken")

    const options = {            //cookies are modifieable only at the server by this option
        httpOnly: true,
        secure: true
    }



    return res
        .status(200)
        .cookie("accessToken", accessToken, options)
        .cookie("refreshToken", refreshToken, options)
        .json(
            new ApiResponse(
                200,
                {
                    user: loggedInUser, accessToken, refreshToken
                },
                "User logged In Successfully"
            )
        )

})



